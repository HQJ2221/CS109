public class packageLearning {
    // package
    // 包是一种命名空间，用于组织类和接口。包可以解决命名冲突的问题。
    // 一个包可以包含多个类和接口，但是只能有一个public类。
    // 包名一般使用小写字母，如果有多个单词，可以使用下划线分隔。

    // 创建包
    // 在src目录下创建一个文件夹，文件夹名就是包名。
    // 选择一个包名称，然后将一个包声明添加到源文件中以进行可用的类声明。
    // 在每个Java源文件中只能有一个包声明，它必须在所有其他声明和语句之前。如 package course.cs102a;
}
